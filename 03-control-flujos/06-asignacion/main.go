package main

import "fmt"

func main() {

	a := 2

	//a = a + 2

	//fmt.Println(a)

	//Operadores de asignación

	//Suma en asignación
	a += 2
	fmt.Println(a)
	//Resta en asignación
	a -= 2
	fmt.Println(a)
	//Multiplicación en asignación
	a *= 5
	fmt.Println(a)
	//División en asignación
	a /= 2
	fmt.Println(a)
	//Modulo en asignación
	a %= 2
	fmt.Println(a)

}
